										<br>
											<b> Los procesos del Centro de Retención Vehicular "CRV"  </b> son: control de los vehículos que ingresan, control de los vehículos residentes, permanencia y retiro vehicular. 
											<br><br>
											Los procesos antes mencionados se encuentran en <b> <a href="http://www.ant.gob.ec/index.php/transito-7/resoluciones-2013/file/2012-resolucion-no-170-dir-2013-ant-reglamento-de-centros-de-retencion-vehicular-a-nivel-nacional" target="_blank">
