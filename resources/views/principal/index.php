 											<br><br>
											SACRV permite a los propietarios de vehículos retenidos ver el motivo de retención y el estado de su vehículo en el momento de ingreso al CRV. Además, le permite la consulta del monto que adeuda por la permanencia del vehículo (hasta la fecha actual). Según el motivo de ingreso se le informa al propietario del vehículo, la documentación que debe adjuntar en el sistema para que le sea devuelto el vehículo, aunque cabe destacar que el sistema no suple la labor que deba hacer el abogado en caso de necesitarlo.
											<br><br>
											SACRV apoya al propietario del vehículo en los procesos que lleva el CRV (antes mencionados). En trámites que incluyan a otras entidades, el propietario es quien las resuelve (están a su gestión).
											En vista que el CRV está bastante alejado, lo que se procura garantizar al propietario es su única venida al momento de retirar el vehículo cuando el encargado haya validado satisfactoriamente la documentación subida
